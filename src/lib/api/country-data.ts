// ISO → emoji flag
export function countryFlag(iso: string): string {
  if (iso.length !== 2) return '\u{1F3F3}\u{FE0F}';
  const offset = 0x1F1E6 - 65; // 'A' = 65
  return String.fromCodePoint(
    iso.charCodeAt(0) + offset,
    iso.charCodeAt(1) + offset,
  );
}
